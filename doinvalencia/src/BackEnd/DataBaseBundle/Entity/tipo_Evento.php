<?php

namespace BackEnd\DataBaseBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * tipo_Evento
 *
 * @ORM\Table(name="tipo__evento")
 * @ORM\Entity(repositoryClass="BackEnd\DataBaseBundle\Repository\tipo_EventoRepository")
 */
class tipo_Evento
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="nombre", type="string", length=45)
     */
    private $nombre;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nombre
     *
     * @param string $nombre
     * @return tipo_Evento
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get nombre
     *
     * @return string 
     */
    public function getNombre()
    {
        return $this->nombre;
    }
}
